
import { onRequest } from 'firebase-functions/v2/https';
import * as admin from 'firebase-admin';
import Stripe from 'stripe';

const stripe = new Stripe(process.env.STRIPE_SECRET_KEY || '', {
  apiVersion: '2023-10-16',
});

const endpointSecret = process.env.STRIPE_WEBHOOK_SECRET || '';

export const processStripeWebhook = onRequest(
  { cors: false },
  async (req, res) => {
    console.log('üîî WEBHOOK - Webhook Stripe re√ßu');
    
    const sig = req.headers['stripe-signature'] as string;
    let event: Stripe.Event;

    try {
      // V√©rifier la signature du webhook
      event = stripe.webhooks.constructEvent(req.body, sig, endpointSecret);
      console.log('‚úÖ WEBHOOK - Signature v√©rifi√©e:', event.type);
    } catch (err: unknown) {
      const errorMessage = err instanceof Error ? err.message : 'Unknown error';
      console.error('‚ùå WEBHOOK - Signature invalide:', errorMessage);
      res.status(400).send(`Webhook signature verification failed: ${errorMessage}`);
      return;
    }

    try {
      switch (event.type) {
        case 'checkout.session.completed':
          await handleCheckoutCompleted(event.data.object as Stripe.Checkout.Session);
          break;
        
        case 'invoice.payment_succeeded':
          await handlePaymentSucceeded(event.data.object as Stripe.Invoice);
          break;
        
        case 'customer.subscription.updated':
          await handleSubscriptionUpdated(event.data.object as Stripe.Subscription);
          break;
        
        case 'setup_intent.succeeded':
          await handleSetupIntentSucceeded(event.data.object as Stripe.SetupIntent);
          break;
        
        default:
          console.log(`üîî WEBHOOK - Type non trait√©: ${event.type}`);
      }

      res.json({ received: true });
    } catch (error: unknown) {
      console.error('‚ùå WEBHOOK - Erreur traitement:', error);
      res.status(500).send('Erreur traitement webhook');
    }
  }
);

async function handleCheckoutCompleted(session: Stripe.Checkout.Session) {
  console.log('üí≥ WEBHOOK - Checkout compl√©t√©:', session.id);
  
  if (session.mode === 'setup' && session.setup_intent) {
    console.log('‚öôÔ∏è WEBHOOK - Processing setup mode session');
    
    try {
      // R√©cup√©rer le setup intent pour obtenir la m√©thode de paiement
      const setupIntent = await stripe.setupIntents.retrieve(session.setup_intent as string);
      console.log('üìã WEBHOOK - SetupIntent r√©cup√©r√©:', setupIntent.id);
      
      if (setupIntent.payment_method && session.customer) {
        console.log('üí≥ WEBHOOK - Attachement de la m√©thode de paiement au client');
        
        // Forcer l'attachement de la m√©thode de paiement au client
        try {
          await stripe.paymentMethods.attach(setupIntent.payment_method as string, {
            customer: session.customer as string,
          });
          console.log('‚úÖ WEBHOOK - M√©thode de paiement attach√©e au client');
        } catch (attachError: any) {
          // Si d√©j√† attach√©e, continuer
          if (attachError.code !== 'resource_already_exists') {
            throw attachError;
          }
          console.log('‚úÖ WEBHOOK - M√©thode de paiement d√©j√† attach√©e');
        }
        
        // D√©finir comme m√©thode par d√©faut
        await stripe.customers.update(session.customer as string, {
          invoice_settings: {
            default_payment_method: setupIntent.payment_method as string,
          },
        });
        
        console.log('‚úÖ WEBHOOK - M√©thode de paiement d√©finie par d√©faut');
      }
    } catch (attachError) {
      console.error('‚ùå WEBHOOK - Erreur lors de l\'attachement:', attachError);
      // Ne pas faire √©chouer le webhook pour cette erreur
    }
    
    // Configuration de paiement r√©ussie
    const campaignId = session.metadata?.campaign_id;
    if (campaignId) {
      await admin.firestore()
        .collection('campaigns')
        .doc(campaignId)
        .update({
          paymentConfigured: true,
          isDraft: false,
          stripeCustomerId: session.customer,
          stripeSetupIntentId: session.setup_intent,
          updatedAt: admin.firestore.FieldValue.serverTimestamp(),
        });
      
      console.log('‚úÖ WEBHOOK - Campagne finalis√©e:', campaignId);
    }
  }
}

